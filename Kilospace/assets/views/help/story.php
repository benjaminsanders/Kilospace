    <h2>The Past</h2>
    <p>
In 2018 a private organization known as Kilospace Industries launched the first mining missions on the lunar surface. Within a decade it held the status of most powerful cooperation, and by 2037 it had accumulated 29 countries under a single "umbrella" nation. By 2040 terrorism, wars, and national disputes were nearly a memory in the past. The Earth was united as "Terra" on July 2nd, 2044.</p>
